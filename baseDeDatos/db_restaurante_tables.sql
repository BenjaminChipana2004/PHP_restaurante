-- =====================================================

CREATE TABLE DB_Factura (
    DB_Factura_ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    serie VARCHAR(255) NOT NULL,
    numero INTEGER NOT NULL,
    fecha DATE NOT NULL,

    subtotal NUMERIC(10,2) NOT NULL,
    igv NUMERIC(10,2) NOT NULL,
    total NUMERIC(10,2) NOT NULL,

    DB_Pedido_ID INTEGER NOT NULL UNIQUE,

    CONSTRAINT FK_DB_Factura_Pedido
        FOREIGN KEY (DB_Pedido_ID)
        REFERENCES DB_Pedido(DB_Pedido_ID)
);

-- =====================================================
-- �NDICES OPCIONALES
