CREATE INDEX IDX_USUARIO_EMAIL
    ON DB_Usuario(email);

CREATE INDEX IDX_PEDIDO_FECHA
    ON DB_Pedido(fecha);

CREATE INDEX IDX_FACTURA_NUMERO
    ON DB_Factura(numero);

CREATE INDEX IDX_PLATO_NOMBRE
    ON DB_Plato(nombre);